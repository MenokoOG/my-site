import { NextResponse } from "next/server";

// Minimal in-memory ICO payload to avoid favicon 404s in development.
const FAVICON_BYTES = new Uint8Array([
    0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x01, 0x01,
    0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x30, 0x00,
    0x00, 0x00, 0x16, 0x00, 0x00, 0x00, 0x28, 0x00,
    0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x02, 0x00,
    0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x08, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0xff, 0xff, 0x00, 0x00, 0x00,
    0x00, 0x00,
]);

export function GET() {
    return new NextResponse(FAVICON_BYTES, {
        headers: {
            "Content-Type": "image/x-icon",
            "Cache-Control": "public, max-age=604800, immutable",
        },
    });
}
